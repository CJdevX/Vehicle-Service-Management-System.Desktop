    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        try {
            connect = Database.openConnection();
            String sql = "INSERT INTO service(name, price, discount, subPrice) VALUES(?, ?, ?, ?)";
            PreparedStatement statement = connect.prepareStatement(sql);
            statement.setString(1, txt_name.getText().toUpperCase());
            statement.setDouble(2, Double.parseDouble(txt_price.getText()));
            if (txt_discount.getText().isEmpty()) {
                statement.setString(3, null);
                statement.setDouble(4, Double.parseDouble(txt_price.getText()));
            } else {
                statement.setDouble(3, Double.parseDouble(txt_discount.getText()));
                double subPrice = Double.parseDouble(txt_price.getText()) - Double.parseDouble(txt_discount.getText());
                statement.setDouble(4, subPrice);
            }
            statement.executeUpdate();
            showNotification("Service Insert Successful", SUCCESS);
            connect.close();

        } catch (Exception e) {
            showNotification("Service Insert Unsuccessful", WARNING);
        }
        loadData();
        clearData();
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        try {
            connect = Database.openConnection();
            String sql = "UPDATE service SET name = ?, price = ?, discount = ?, subPrice = ? WHERE serviceID = ?";
            PreparedStatement statement = connect.prepareStatement(sql);
            statement.setString(1, txt_name.getText().toUpperCase());
            statement.setDouble(2, Double.parseDouble(txt_price.getText()));
            if (txt_discount.getText().isEmpty()) {
                statement.setString(3, null);
                statement.setDouble(4, Double.parseDouble(txt_price.getText()));
            } else {
                statement.setDouble(3, Double.parseDouble(txt_discount.getText()));
                double subPrice = Double.parseDouble(txt_price.getText()) - Double.parseDouble(txt_discount.getText());
                statement.setDouble(4, subPrice);
            }
            statement.setInt(5, Integer.parseInt(txt_id.getText()));
            statement.executeUpdate();
            showNotification("Service Update Successful", SUCCESS);
            connect.close();

        } catch (Exception e) {
            showNotification("Service Update Unsuccessful", WARNING);
        }
        loadData();
        clearData();
        disableButton();
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", null, JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.YES_OPTION) {
            try {
                connect = Database.openConnection();
                String sql = "DELETE FROM service WHERE serviceID = " + txt_id.getText();
                PreparedStatement statement = connect.prepareStatement(sql);
                statement.executeUpdate();
                showNotification("Service Delete Successful", SUCCESS);
                connect.close();

            } catch (Exception e) {
                showNotification("Service Delete Successful", WARNING);
            }
            loadData();
            clearData();
            disableButton();
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

