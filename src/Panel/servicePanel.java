    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        try {
            connect = Database.openConnection();
            String sql = "SELECT * FROM service WHERE ";
            sql = sql + "serviceID LIKE '%" + search.getText() + "%' OR ";
            sql = sql + "name LIKE '%" + search.getText() + "%'";
            PreparedStatement statement = connect.prepareStatement(sql);

            ResultSet result = statement.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tbl_service.getModel();
            model.setRowCount(0);

            while (result.next()) {
                String id = result.getString("ServiceID");
                String name = result.getString("name");
                String price = result.getString("price");
                String discount = result.getString("discount");
                String subPrice = result.getString("subPrice");

                model.addRow(new Object[]{id, name, price, discount, subPrice});
            }
            connect.close();

        } catch (Exception e) {
            showNotification("Error", WARNING);
        }
    }//GEN-LAST:event_searchKeyReleased

